// Upscale a bitmap font by an integer factor — each '#' becomes a scale×scale
// block. Used to generate larger chunky variants from the base letterforms.
const upscaleBitmapFont = (bitmap, scale) => {
  const out = {};
  for (const ch in bitmap) {
    out[ch] = bitmap[ch].flatMap(row => {
      const expanded = row.split('').flatMap(c => Array(scale).fill(c)).join('');
      return Array(scale).fill(expanded);
    });
  }
  return out;
};

